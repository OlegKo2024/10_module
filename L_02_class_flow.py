import threading
import time
lock = threading.Lock()

class MyThread(threading.Thread):
    def __init__(self, name, counter, delay=1):
        threading.Thread.__init__(self)
        self.name = name
        self.counter = counter
        self.delay = delay
# Да, вы правильно понимаете. В методе run() вы вызываете метод timer() с использованием значений, хранящихся в
# свойствах экземпляра, которые были инициализированы в методе __init__().
#
# 1. Позиционные параметры: Когда вызываете метод self.timer(self.name, self.counter, self.delay), передаете значения,
# хранящиеся в свойствах экземпляра, в качестве позиционных аргументов метода timer. Эти значения берутся из атрибутов,
# которые вы настроили в __init__().
# 2. Если бы вы написали self.timer(self.name, self.counter, self.delay) с использованием self.name, self.counter и
# self.delay, то переданные параметры действительно пришли бы из конструкторов и были бы свойствами объекта. То есть,
# значения будут получены из тех же атрибутов, но при передаче в метод timer они будут локальными.
# 3. Локальные переменные: Когда мы говорим, что параметры name, counter и delay являются локальными, это означает,
# что они существуют только в контексте метода timer и недоступны за его пределами. Это означает, что, как только метод
# завершает выполнение, эти переменные больше не будут доступны. Локальные переменные могут принимать свои значения на
# момент вызова метода, но не сохраняют их после выхода из метода. В вашем случае, когда вы вызываете timer, создаются
# локальные переменные, а присвоение name = self.name и т. д. будет доступно в методе timer только в том контексте, где
# он вызывается.
# Таким образом, использование локальных переменных или атрибутов экземпляра зависит от вашего выбора и архитектуры
# вашего класса. Если вам нужно часто использовать эти значения, может быть лучше работать с атрибутами экземпляра,
# чтобы избежать передачи их через аргументы в методах.
    def timer(self, name, counter, delay):
        while counter:
            time.sleep(delay)
            print(f'{name}, {time.ctime(time.time())}')
            counter -= 1

    def run(self):
        print(f'Поток {self.name} запущен')
        with lock:
            self.timer(self.name, self.counter, self.delay)
        print(f'Поток {self.name} завершен')


thread1 = MyThread('t1', 5, 3)
thread2 = MyThread('t2', 3, 2)
thread3 = MyThread('t3', 2, 5)

thread1.start()
thread2.start()
thread3.start()


# В первом примере используется super().__init__(), что является предпочтительным и современным способом вызова
# конструктора родительского класса. Во втором примере вызывается Thread.__init__(self), что также корректно, но менее
# рекомендуемо, так как это менее гибко и не использует механизм разрешения имени.
# Таким образом, разница заключается в стиле:
# 1. super().__init__() - более предпочтительно, подходит для многословного наследования и улучшает читаемость кода.
# 2. Thread.__init__(self) - более старый стиль, который требует явного указания экземпляра, но все равно работает.

