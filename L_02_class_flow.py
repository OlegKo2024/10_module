from threading import Thread
from datetime import datetime
import requests

class Getter(Thread):
    THE_URL = 'https://binaryjazz.com/wp-json/generator/v1/genre/'
    res = []

    def run(self):
        response = requests.get(self.THE_URL)
        Getter.res.append(response.json())
# Создание и запуск потоков:
# - В цикле for i in range(10): создается 10 экземпляров класса Getter.
# - Каждый экземпляр выполняет thread.start(), что вызывает метод run() в каждом отдельном потоке.
# ### Вопросы и ответы:
# 1. Создание 10 объектов класса Getter: - Да, в цикле создается 10 объектов (экземпляров) класса Getter. Каждый раз,
# когда вы вызываете Getter(), создается новый объект.
# 2. Экземпляр thread: - Внутри цикла переменная thread будет ссылаться на последний созданный экземпляр. Каждый новый
# объект Getter создается, и его ссылка заменяет предыдущую. Тем не менее каждый поток продолжит работать независимо и
# асинхронно, даже после завершения цикла.
# - Важно понимать, что переменные в Python создают ссылки на объекты. Это значит, что thread будет хранить указатель на
# последний созданный объект, но все потоки, которые были запущены, будут работать в своем собственном контексте, даже
# если разница в их идентификации может быть неочевидна.
# 3. Идентификация потоков:
# - Вы можете идентифицировать каждый поток, используя их индексы, либо просто добавляя их в список threads, как это
# сделано в коде (threads.append(thread)). Теперь у вас есть доступ к каждому конкретному потоку через список threads.
# Вы можете обращаться к ним по индексу, или итерироваться по списку. Каждый экземпляр будет продолжать выполнять свой
# ресурс, даже если переменная thread указывает только на последний экземпляр.
# 4. Завершение потоков:
# - После создания и запуска потоков, код ждет завершения всех потоков, используя thread.join(). Это гарантирует, что
# основной поток не завершится до тех пор, пока все потоки Getter не выполнят свой код.
# 5. Результат: - После завершения работы всех потоков результат каждой HTTP-запроса будет собран внутри класса Getter

# Итак. Каждый экземпляр работает независимо, хотя все они создаются в одном цикле и используют одну и ту же переменную
# thread, ссылки на созданные экземпляры сохраняются в списке threads, что позволяет вам работать с каждым потоком позже

threads = []

start_time = datetime.now()

for i in range(10):
    thread = Getter()
    thread.start()  # Метод run() запускается автоматом, когда вы вызываете метод start() для экземпляра класса Getter.
    threads.append(thread)

# Как работает многопоточность в Python с использованием класса Thread:
# 1. Наследование от Thread:
# Класс Getter наследует от класса Thread. Это означает, что экземпляры Getter становятся потоками. Переопределяя метод
# run(), вы определяете, что должен делать поток, когда он запускается. В данном случае, поток будет выполнять код,
# написанный в run(), когда его запустят.
# Создание экземпляров: - в цикле for i in range(10): создаются 10 экземпляров класса Getter:
# При вызове thread.start(), Python автоматически вызывает метод run() для этого экземпляра в новом потоке.

# Стандартное поведение:
# - Когда вы создаете класс, который наследует от Thread, вы можете переопределить метод run().
# - Когда вы вызываете метод start() на экземпляре класса, Python создает новый поток и автоматом вызывает метод run()
# этого экземпляра в контексте нового потока.
# 2. Почему именно run()?
# - Это часть спецификации класса Thread. Это стандарт. метод, который ожидается для выполнения кода в отдельном потоке.
# - Если в классе Getter переопределить метод run(), Python поймет, что должен вызывать этот метод при запуске потока.
# 3. Если переименовать метод:
# - Если вы назовете метод, который хотите, чтобы выполнялся в потоке, например, ban() вместо run(), то по умолчанию
# Python не будет знать, что вы хотите выполнить этот метод при вызове start().
# - В этом случае, если вы попытаетесь просто вызвать thread.start(), ничего не произойдет: поток будет создан, но
# код внутри метода ban() не будет выполнен. Это связано с тем, что механизм потоков настроен именно на метод run().
# ### Как это устроено на уровне кода: Когда вы вызываете thread.start(), происходит следующий процесс (грубая схема,
# чтобы дать представление): def start(self):
#                               self._start_new_thread(self.run)  # <== В этом месте вызывается именно run()

